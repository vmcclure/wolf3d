#include "wolf3d.h"

void	set_pxl(char *str, int x, int y, int clr)
{
	int i;

	i = x + y;
	str += i;
	str[0] = (char)clr % 256;
	clr /= 256;
	str[1] = (char)clr % 256;
	clr /= 256;
	str[2] = (char)clr % 256;
}
